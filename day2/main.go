var shapeScores = map[byte]int{
	'X': 1,
	'Y': 2,
	'Z': 3,
}

var outcomeScore = map[byte]int{
	'w': 6,
	't': 3,
	'l': 0,
}

var outcomes = map[string]byte{
	"A X": 't',
	"A Y": 'w',
	"A Z": 'l',

	"B X": 'l',
	"B Y": 't',
	"B Z": 'w',

	"C X": 'w',
	"C Y": 'l',
	"C Z": 't',
}

// a = rock, b = paper, c = scissors
// x = rock, y = paper, c = scissors
// x = lose, y = tie, c = win

var choice = map[string]byte{
	"A X": 'Z',
	"A Y": 'X',
	"A Z": 'Y',

	"B X": 'X',
	"B Y": 'Y',
	"B Z": 'Z',

	"C X": 'Y',
	"C Y": 'Z',
	"C Z": 'X',
}

func main() {
	rounds := util.ReadInput(Input, "\n")
	fmt.Printf("first: %d\n", first(rounds))
	fmt.Printf("second: %d\n", second(rounds))
}

func first(rounds []string) int {
	var score int
	for _, r := range rounds {
		outcome := outcomes[r]
		score += outcomeScore[outcome] + shapeScores[r[2]]
	}

	return score
}

func second(rounds []string) int {
	var score int
	for _, r := range rounds {
		play := choice[r]
		doRound := string([]byte{r[0], ' ', play})
		outcome := outcomes[doRound]
		score += outcomeScore[outcome] + shapeScores[doRound[2]]
	}

	return score
}

